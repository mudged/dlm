#!/usr/bin/env node
/**
 * Generates backend/internal/seed/python_routine_samples_gen.go from
 * web/lib/pythonRoutineSamples.ts so SQLite seed bytes match the TS source of truth.
 */
import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const tsPath = join(root, "web/lib/pythonRoutineSamples.ts");
const outPath = join(root, "backend/internal/seed/python_routine_samples_gen.go");

const src = readFileSync(tsPath, "utf8");

function extractTemplate(name) {
  const prefix = `export const ${name} = \``;
  const i = src.indexOf(prefix);
  if (i < 0) {
    throw new Error(`missing export ${name} in ${tsPath}`);
  }
  let j = i + prefix.length;
  while (j < src.length) {
    const ch = src[j];
    if (ch === "`") {
      return src.slice(i + prefix.length, j);
    }
    j++;
  }
  throw new Error(`unclosed template for ${name}`);
}

const rows = [
  {
    ts: "PYTHON_SAMPLE_GROWING_SPHERE_SOURCE",
    goConst: "growingSphereSource",
    name: "Growing sphere (sample)",
    description: "REQ-032 novice sample: sphere grows 10 s in scene centre.",
  },
  {
    ts: "PYTHON_SAMPLE_SWEEPING_CUBOID_SOURCE",
    goConst: "sweepingCuboidSource",
    name: "Sweeping cuboid (sample)",
    description: "REQ-032 novice sample: slab sweeps floor to ceiling.",
  },
  {
    ts: "PYTHON_SAMPLE_RANDOM_COLOUR_CYCLE_ALL_SOURCE",
    goConst: "randomColourCycleSource",
    name: "Random colour cycle — all lights (sample)",
    description: "REQ-032 novice sample: ~1 s full-scene random colours.",
  },
];

let go = `// Code generated by scripts/gen-python-routine-samples-go.mjs from web/lib/pythonRoutineSamples.ts. DO NOT EDIT.

package seed

`;

for (const r of rows) {
  const body = extractTemplate(r.ts);
  if (body.includes("`")) {
    throw new Error(`sample ${r.ts} contains backtick — fix generator or source`);
  }
  go += `const ${r.goConst} = \`${body}\`

`;
}

go += `// DefaultPythonRoutineRows returns the three REQ-032 definitions for store seeding (names, descriptions, python_source).
func DefaultPythonRoutineRows() [3]struct {
	Name        string
	Description string
	Source      string
} {
	return [3]struct {
		Name        string
		Description string
		Source      string
	}{
		{${JSON.stringify(rows[0].name)}, ${JSON.stringify(rows[0].description)}, ${rows[0].goConst}},
		{${JSON.stringify(rows[1].name)}, ${JSON.stringify(rows[1].description)}, ${rows[1].goConst}},
		{${JSON.stringify(rows[2].name)}, ${JSON.stringify(rows[2].description)}, ${rows[2].goConst}},
	}
}
`;

writeFileSync(outPath, go, "utf8");
console.log("wrote", outPath);
